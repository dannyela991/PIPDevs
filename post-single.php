                        <p>Isso permitirá aos alunos de hoje, amanhã e sempre, em todo o país, blindar a construção de seus projetos de vida com reserva estratégica, conseguida graças a uma conscientização comportamental contra o consumismo em excesso. Isso sim permitirá que todos estejam preparados tanto para poder aproveitar oportunidades únicas, quanto para se salvar de calamidades extremas! E assim o determina a Base Nacional Comum Curricular (BNCC), embora ela ainda não regulamente a garantia de que esse direito seja ofertado de forma obrigatória também no Ensino Médio e infantil. No Ensino Fundamental já é um conteúdo obrigatório desde 2017.</p>
                        <p>Não é da noite para o dia, nem isolada e esporadicamente, sem uma disciplina escolar, que vamos gerar hábitos que desenvolvam sistematicamente o espírito empreendedor, a autonomia, o hábito de planejamento de curto, médio e longo prazos ou estratégias para garantir a sustentabilidade de tudo isso. É necessário um trabalho pedagógico continuado, estamos falando de potencializar posturas e atitudes construtivas, uma revolução socioemocional e, sobretudo, comportamental. Se começarmos já, ainda dá tempo de pouparmos as próximas gerações de terem que improvisar, atônitas, reações à efervescente mutação dos valores globais.</p>
                        <p>Não há segredo: há a necessidade de resgatar o ensino sistemático do velho e bom planejamento, aprendendo desde cedo a poupar, com perseverança, em nome de sonhos e propósitos, blindando pouco a pouco nosso empoderamento progressivo e indispensável, rumo a um futuro digno, próspero, sustentável e de independência financeira, algo bem diferente do que observamos na maioria da população mais madura atual.</p>
                        <p>Se a escola dos seus filhos, enteados, sobrinhos, afilhados, filhos de amigos, vizinhos, conhecidos... não ensina Educação Financeira baseada em metodologia chancelada por reconhecimento científico, acadêmico e vivencial, é hora de que toda a sociedade exija que passe a ensinar e já!! Uma pesquisa comprova os resultados eficazes no comportamento de alunos de Ensino Básico e foi feita pela Unicamp em parceria com o Instituto Axxus, requerida pela ABEFIN, junto a escolas que já adotam transversalmente a Educação Financeira nos moldes conscientizadores recomendados pela BNCC (Base Nacional Comum Curricular).*</p>
                        <p>Como será a reação de instituições financeiras que faturam com juros abusivos, graças ao endividamento da maioria de seus clientes, se estes últimos passarem a garantir e a manter à salvo massivamente a reserva estratégica, a evitar o endividamento, a planejar desde cedo o digno bem-estar pós futuro afastamento do mercado laboral? E quanto ao empoderamento psicológico de ir além, ou seja, ao longo dessa trajetória, construir um espírito empreendedor, independente, colecionador de realizações de curto, médio e longo prazos?</p>
                        <p>A Globalização nos aproximou de exemplos inspiradores e bem-sucedidos de outras culturas e países que há muitas décadas já não contam massivamente com décimo terceiro salário, férias remuneradas, Fundo de Garantia, Previdência Governamental, benefícios empregatícios tais como auxílio alimentação, transporte, assistência médica...O novo normal mundial segue essa tendência. Como garantir bem-estar sem auto-gestão aprendida desde cedo, tendo em conta que a geração de adultos pós pandemia tampouco terá como assegurar heranças milionárias para deixar como legado para as crianças e jovens de hoje? </p>
                        <p>É nossa obrigação, dos adultos de hoje, lutar pelo jovem de amanhã, inspirando-o através do exemplo, pois nós também devemos aplicar a Educação Financeira em nossas vidas, uma vez que queremos viver com qualidade, chegando a ser centenários, sem onerar, sobrecarregar ou estagnar os familiares mais jovens. O futuro já começou!!</p>

