                        <p>
                        “Se a partir de hoje você não recebesse mais o seu ganho mensal, por quanto tempo conseguiria manter seu atual padrão de vida?"</p>
                        <p>Adivinha a partir de qual momento devemos começar a prepará-los para uma resposta feliz à pergunta acima?  Já! Sim. E tem mais: pasme, isso só depende da sua colaboração!! Você deve estar se perguntando: Como? Por quê? Continue lendo e entenderá.</p>
                        <p>Desenvolver hábitos saudáveis e mantê-los de forma sustentável ao longo do tempo requer mais que necessidade, disciplina e convicção, requer a motivação de ver resultados constantes, positivos e metodológicos. Quer estímulo maior que realizar sonhos nobres, regularmente, em todos os momentos de nossas vidas? Mas isso só é possível se aprendido desde cedo, massivamente, por meio da Educação Financeira, ensinada nas escolas de forma obrigatória, em todo o ensino básico nacional.</p>

